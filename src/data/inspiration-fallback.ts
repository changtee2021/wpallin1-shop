export const INSPIRATION_FALLBACK_ROOMS: InspirationRoomDto[] =
  INSPIRATION_FALLBACK_RAW.map((room) => ({
    ...room,
    ...seedEngagementForRoom(room.id),
    detailImages: [],
  }));

export function getInspirationFallbackBySlug(
  slug: string,
): InspirationRoomDto | null {
  return INSPIRATION_FALLBACK_ROOMS.find((r) => r.slug === slug) ?? null;
}

export async function resolveInspirationRooms(
  fetchFn: () => Promise<InspirationRoomDto[]>,
): Promise<InspirationRoomDto[]> {
  const rooms = await fetchFn().catch(() => []);
  return rooms.length ? rooms : INSPIRATION_FALLBACK_ROOMS;
}
